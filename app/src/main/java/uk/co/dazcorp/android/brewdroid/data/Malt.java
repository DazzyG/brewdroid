
package uk.co.dazcorp.android.brewdroid.data;

public class Malt {

    private Amount amount;
    private String name;

    /**
     * 
     * @return
     *     The amount
     */
    public Amount getAmount() {
        return amount;
    }

    /**
     * 
     * @param amount
     *     The amount
     */
    public void setAmount(Amount amount) {
        this.amount = amount;
    }

    /**
     * 
     * @return
     *     The name
     */
    public String getName() {
        return name;
    }

    /**
     * 
     * @param name
     *     The name
     */
    public void setName(String name) {
        this.name = name;
    }

}
